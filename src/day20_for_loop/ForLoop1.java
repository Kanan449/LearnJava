package day20_for_loop;

public class ForLoop1 {
public static void main(String[] args) {
	for (int i = 0; i < 3; i++) {
		System.out.println("Love Java");
	}
	
	
	
	for (int j = 5; j<=15; j++) {
		System.out.print(j+" ");
	}
	
	System.out.println();
	
	for (int k = 20; k>=10;k-=1) {
		System.out.print(k+" ");
	}
	
	
	
	
}
}
