package day03_helloworld_variables;

public class VariableDeclarations {
	public static void main(String[]args) {
		//Declare single variable
		int apples;
		//Declare multiple variable in single statement
		int bananas, oranges, mangos; 
		
		mangos = 24;
		
		//Declare variable and assign value in single statement
		int kiwi = 14; 
		
		System.out.println(kiwi);
		System.out.println(mangos);
		
		//Declare and assign value to multiple variables in a single statement. 
		
		int watermelon = 11, grapes = 100, cherries = 12, apricots;
		
		int count = 123;
		
		count = 55; 
		
		System.out.println(count);
		
	}

}
