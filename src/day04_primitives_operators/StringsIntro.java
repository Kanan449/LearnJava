package day04_primitives_operators;

public class StringsIntro {
	public static void main(String[] args) {
		//declare String variable : todayyClass
		
		String todaysClass;
		todaysClass = "Java Programming";
		String city = "Tysons, VA";
		
		System.out.println(todaysClass);
		System.out.println(city);
		
		//String must be declared in double quotes. Example; "Hello"
		
		String food, sevirem;
		food = "Kabob";
		sevirem = "chox dadlidi";
		
		System.out.print(food);
		System.out.print(" ");
		System.out.println(sevirem);
		System.out.println("New Yorkda" + food + sevirem);
	}

}
